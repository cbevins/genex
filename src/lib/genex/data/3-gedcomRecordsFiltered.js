import fs from 'fs'
import { selectLines } from '../gedcom/selectLines.js'
import { _gedcomContextFilterMASTER } from './customized/_gedcomContextFilterMASTER.js'
import { _gedcomRecordsAll } from './generated/gedcom/_gedcomRecordsAll.js'

const time1 = new Date()
const progName = (process.argv[1]).split('\\').pop()
const filterFile = './customized/_gedcomContextFilterMASTER.js'
const inputFile = './generated/gedcom/_gedcomRecordsAll.js'
const varName = '_gedcomRecordsFiltered'
const outputFile = `./generated/gedcom/${varName}.js`

const records = selectLines(_gedcomRecordsAll, new Set(_gedcomContextFilterMASTER))

const head = [
    `// auto-generated by ${progName} on ${new Date().toLocaleString()}`,
    `// from '${inputFile}'`,
    `export const ${varName} = [`
]
let js = head.join('\n') + '\n'
for(let i=0; i<records.length; i++) js += JSON.stringify(records[i]) + ',\n'
js += ']\n'
fs.writeFile(outputFile, js, function (err) { if (err) throw err })

console.log(`\n${progName}`)
console.log(`    1 - read ${_gedcomRecordsAll.length} [<GEDCOM record>] elements from '${inputFile}'.`)
console.log(`    2 - applied context filter from '${filterFile}'.`)
console.log(`    3 - wrote ${records.length} [<GEDCOM record>] elements into '${outputFile}'.`)
console.log(`    Successfully completed in ${new Date()-time1} msec`)
